               ORG  27648
               DUMP 32768

linebuff:      DEFS 64           ;note at block address
scstartp:      DEFB 5
scstarto:      DEFW 32768
cmstartp:      DEFB 1
cmstarto:      DEFW 32768
numcount:      DEFB 0

entry:         DI
               LD   A,5           ;set start of SC_file
               LD   HL,32768
               LD   (scstartp),A
               LD   (scstarto),HL
               LD   A,1           ;set start of COMET file
               OUT  (251),A
               LD   (HL),0
               INC  HL
               LD   (cmstartp),A
               LD   (cmstarto),HL
               LD   IY,numcount   ;=holds numberic expresion bytes
nextline:      LD   A,(scstartp)
               OUT  (251),A
               LD   HL,(scstarto)
               LD   DE,linebuff+1
               LD   C,(HL)        ;fetch line no.
               INC  HL
               LD   B,(HL)
               INC  HL
               LD   A,B           ;exit if line no. is 65535
               AND  C
               INC  A
               JP   Z,exit
               LD   C,(HL)        ;line lenght
               INC  HL
               LD   A,(HL)        ;instruction type
               INC  HL
               PUSH AF            ;move possible label
               AND  15
               JR   Z,inssel
               LD   (DE),A
               INC  DE
               LD   B,C           ;save line lenght temp. in B
               LD   C,A           ;BC label lenght
               LD   A,B
               LD   B,0
               LDIR
               LD   C,A           ;C line lenght for later use
inssel:        POP  AF
               RLA
               JR   C,linedone
               RLA
               JP   C,directives
               RLA
               JP   C,remarks
               RLA
               JP   C,operands
               LD   A,(HL)
               INC  HL
               CP   203          ;Fetch right table
               JR   NZ,testDD
               LD   A,(HL)
               INC  HL
               PUSH HL
               LD   HL,tabCB
               JR   tokcont
testDD:        CP   221
               JR   NZ,testED
               LD   A,(HL)
               SUB  9
               INC  HL
               PUSH HL
               LD   HL,tabDD
               JR   tokcont
testED:        CP   237
               JR   NZ,testFD
               LD   A,(HL)
               SUB  64
               INC  HL
               PUSH HL
               LD   HL,tabED
               JR   tokcont
testFD:        CP   253
               JR   NZ,opcode
               LD   A,(HL)
               SUB  9
               INC  HL
               PUSH HL
               LD   HL,tabFD
               JR   tokcont
opcode:        INC  HL
               PUSH HL
               LD   HL,tab00
tokcont:       AND  A          ;find instuction in table
               JR   Z,moveins
               LD   B,A
nxtins:        LD   A,(HL)
               INC  HL
               AND  A
               JR   NZ,nxtins
               DJNZ nxtins
moveins:       LD   A,(HL)     ;move instruction to linebuff
               AND  A
               JR   Z,insdone
               LD   (DE),A
               INC  DE
               INC  HL
               JR   moveins
insdone:       POP  HL
linedone:      BIT  6,H       ;move the line to the end of the
               JR   Z,lindon  ;new source file
               RES  6,H
               LD   A,(scstartp)
               INC  A
               LD   (scstartp),A
lindon:        LD   (scstarto),HL
               LD   HL,(cmstarto)
               EX   DE,HL
               LD   (HL),0
               LD   A,L
               INC  A
               LD   L,0
               LD   (HL),A
               LD   B,L
               LD   C,A
               LD   A,(cmstartp)
               OUT  (251),A
               LDIR
               EX   DE,HL
               BIT  6,H
               JR   Z,movlin
               RES  6,H
               INC  A
               LD   (cmstartp),A
movlin:        LD   (cmstarto),HL
               JP   nextline
exit:          LD   A,(cmstartp)   ;file is converted
               OUT  (251),A
               LD   HL,(cmstarto)
               LD   (HL),0         ;set end marker
               INC  HL
               LD   (cmstarto),HL  ;set end
               EI
               RET
remarks:
               LD   A,-4
               ADD  C
               LD   C,A
               LD   B,0
               LD   A,";"
               LD   (DE),A
               LD   A,1
               INC  DE
               LD   (DE),A
               INC  DE
               JR   Z,linedone
               LDIR
               JR   linedone
directives:
               LD   A,(HL)
               INC  HL
               SUB  9
               JR   C,skipdir
               CP   5
               JR   NZ,dircont
               LD   A,150          ;DEFM
               LD   (DE),A
               INC  DE
               LD   A,(HL)
               INC  HL
               LD   B,0
               LD   C,A
               LDIR
               LD   A,""
               LD   (DE),A
               INC  DE
               JR   linedone
dircont:       PUSH HL
               LD   HL,tabdir
               JR   transins
skipdir:       INC  A
               JR   Z,restart
               LD   HL,(scstarto)
               LD   B,0
               ADD  HL,BC
               JP   linedone
restart:       LD   A,218          ;RST
               LD   (DE),A
               INC  DE
               INC  HL
               LD   A,(HL)
               PUSH HL
               LD   H,0
               LD   L,A
               CALL get100
               POP  HL
               INC  HL
               INC  HL
               JP   linedone
operands:
               LD   A,(HL)
               DEC  A
               INC  HL
               PUSH HL
               LD   HL,tabop
transins:      AND  A
               JR   Z,moveinsnum
               LD   B,A
nxtinsnum:     LD   A,(HL)
               INC  HL
               AND  A
               JR   NZ,nxtinsnum
               DJNZ nxtinsnum
moveinsnum:    LD   A,(HL)
               AND  A
               JR   Z,insdon2
               CP   255
               JR   NZ,storins
               EX   (SP),HL
               CALL getexpres
               EX   (SP),HL
               INC  HL
               JR   moveinsnum
storins:       LD   (DE),A
               INC  DE
               INC  HL
               JR   moveinsnum
insdon2:       POP  HL
               JP   linedone
getexpres:
               LD   A,(HL)    ;get expression lenght
               LD   (IY),A    ;set count
               INC  HL
nxtloop:       LD   A,(HL)    ;get type
               DEC  (IY)
               INC  HL
               CP   ""
               JR   NZ,testbin
               LD   (DE),A
               INC  DE
               LD   A,(HL)
               CP   ""
               JR   Z,quote
               LD   (DE),A
               INC  DE
               LD   A,""
quote:         LD   (DE),A
               INC  DE
               JR   nxtexpres
testbin:       CP   "%"
               JR   NZ,smalhex
               LD   (DE),A
               INC  DE
               LD   C,(HL)
               LD   B,8
storebin:      RL   C
               LD   A,"0"
               ADC  0
               LD   (DE),A
               INC  DE
               DJNZ storebin
               JR   nxtexpres
smalhex:       CP   "#"
               JR   NZ,smaldec
               LD   A,"&"
               LD   (DE),A
               INC  DE
               LD   C,(HL)
               CALL hexbyte
               JR   nxtexpres
smaldec:       CP   "*"
               JR   NZ,testbig
               LD   A,(HL)
               PUSH HL
               LD   H,0
               LD   L,A
               CALL get100
               POP  HL
               JR   nxtexpres
testbig:       CP   128
               JR   NC,dubhex
               CP   "A"
               JR   C,nocaps
               CP   "Z"+1
               JR   NC,nocaps
               OR   32
nocaps:        LD   (DE),A
               INC  DE
               LD   A,(numcount)
               AND  A
               JR   NZ,nxtloop
               RET
dubhex:        CP   "#"+128
               JR   NZ,dubdec
               LD   A,"&"
               LD   (DE),A
               INC  DE
               LD   B,(HL)
               INC  HL
               DEC  (IY)
               LD   C,(HL)
               CALL hexbyte
               LD   C,B
               CALL hexbyte
               JR   nxtexpres
dubdec:        LD   C,(HL)
               INC  HL
               DEC  (IY)
               LD   B,(HL)
               PUSH HL
               LD   H,B
               LD   L,C
               CALL get10000
               POP  HL
nxtexpres:     INC  HL
               DEC  (IY)
               JP   NZ,nxtloop
               RET
hexbyte:
               LD   A,C
               RRCA
               RRCA
               RRCA
               RRCA
               AND  15
               ADD  "0"
               CP   "9"+1
               JR   C,hexnum1
               ADD  7
hexnum1:       LD   (DE),A
               INC  DE
               LD   A,C
               AND  15
               ADD  "0"
               CP   "9"+1
               JR   C,hexnum2
               ADD  7
hexnum2:       LD   (DE),A
               INC  DE
               RET
get10000:
               XOR  A
               EX   AF,AF'
               LD   BC,10000
               CALL divider
               LD   BC,1000
               CALL divider
               JR   get100+2
get100:        XOR  A
               EX   AF,AF'
               LD   BC,100
               CALL divider
               LD   C,10
               CALL divider
               LD   A,L
               JR   divcont
divider:       XOR  A
               SBC  HL,BC
               JR   C,divend
               INC  A
               JR   divider+1
divend:        ADD  HL,BC
               AND  A
               JR   NZ,divcont
               EX   AF,AF'
               JR   Z,divex
               EX   AF,AF'
divcont:       ADD  "0"
               LD   (DE),A
               INC  DE
               EX   AF,AF'
               INC  A
divex:         EX   AF,AF'
               RET

